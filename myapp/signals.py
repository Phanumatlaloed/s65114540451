        followers = instance.user.followers.all()  # ‚úÖ ‡πÄ‡∏õ‡∏•‡∏µ‡πà‡∏¢‡∏ô‡∏à‡∏≤‡∏Å `author` ‡πÄ‡∏õ‡πá‡∏ô `user`
